const mongoose = require("mongoose")

const FormSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
})

const ContactModel = mongoose.model("forms", FormSchema)
module.exports = ContactModel
