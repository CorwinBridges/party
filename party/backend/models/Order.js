const mongoose = require("mongoose")

const OrdersSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
})

const OrderModel = mongoose.model("orders", OrdersSchema)
module.exports = OrderModel
